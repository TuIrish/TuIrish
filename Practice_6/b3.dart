enum Gender {
  nam,
  nu,
  khac,
}
//Lớp enum là một cấu trúc dữ liệu trong lập trình được sử dụng để đại diện cho 
//một tập hợp các giá trị cố định và rõ ràng. Trong một enum, các giá trị có thể
//được định nghĩa trước và được sử dụng để biểu diễn các loại khác nhau.
//Các giá trị trong một enum thường được gọi là các thành viên hoặc các phần tử enum.
void main() {
  print(Gender.nam);
  print(Gender.nu); 
  print(Gender.khac);
}
